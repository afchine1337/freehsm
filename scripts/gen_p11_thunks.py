# Output 2 --- src/gen/fhsm_dispatch.c
# ---------------------------------------------------------------------------
def gen_dispatch(mechs: tuple[Mech, ...], profile: str) -> str:
    sorted_mechs = sorted(mechs, key=lambda m: m.value)
    # Collect unique handler symbols that are not the universal reject
    real_handlers = sorted({m.handler for m in mechs
                            if m.handler != "dispatch_reject_fips"})
    lines = [
        "/* AUTO-GENERATED by scripts/gen_p11_thunks.py --- DO NOT EDIT. */",
        f"/* Profile : {profile} */",
        "",
        "#include \"fhsm_pkcs11_mechanisms.h\"",
        "#include \"fhsm_audit.h\"",
        "#include \"fhsm_mode.h\"",
        "",
        "#include <stddef.h>",
        "",
        "/* Forward decl : legacy dispatcher (weak symbol). */",
        "__attribute__((weak)) fhsm_rv_t fhsm_legacy_dispatch(",
        "    unsigned long session, unsigned long key,",
        "    const void *params, size_t plen,",
        "    fhsm_slice_t in, uint8_t *out, size_t *outlen);",
        "",
        "/* Default reject handler --- runtime mode-sensitive.",
        " *  - FIPS strict : FHSM_RV_FIPS_NOT_APPROVED",
        " *  - Legacy mode : delegate to fhsm_legacy_dispatch if present. */",
        "fhsm_rv_t dispatch_reject_fips(unsigned long session, unsigned long key,",
        "                                const void *params, size_t plen,",
        "                                fhsm_slice_t in, uint8_t *out, size_t *outlen)",
        "{",
        "    if (fhsm_mode_is_fips()) {",
        "        (void)session; (void)key; (void)params; (void)plen;",
        "        (void)in; (void)out; (void)outlen;",
        "        return FHSM_RV_FIPS_NOT_APPROVED;",
        "    }",
        "    if (fhsm_legacy_dispatch != NULL) {",
        "        return fhsm_legacy_dispatch(session, key, params, plen,",
        "                                      in, out, outlen);",
        "    }",
        "    (void)session; (void)key; (void)params; (void)plen;",
        "    (void)in; (void)out; (void)outlen;",
        "    return FHSM_RV_FIPS_NOT_APPROVED;",
        "}",
        "",
        "/* ---- Weak stubs for every approved handler -----------------",
        " *",
        " * Each handler below is declared __attribute__((weak)) so the",
        " * concrete implementation (e.g. src/dispatch/fhsm_dispatch_aes.c)",
        " * can override it at link time. While that file is missing, the",
        " * stub returns FHSM_RV_FUNCTION_FAILED so the linker still",
        " * resolves the dispatch table. This lets the project build and",
        " * the smoke tests run before every mechanism has its production",
        " * implementation in place.                                       */",
        "",
    ]
    for h in real_handlers:
        lines += [
            f"__attribute__((weak)) fhsm_rv_t {h}(",
            "    unsigned long session, unsigned long key,",
            "    const void *params, size_t plen,",
            "    fhsm_slice_t in, uint8_t *out, size_t *outlen)",
            "{",
            "    (void)session; (void)key; (void)params; (void)plen;",
            "    (void)in; (void)out; (void)outlen;",
            "    return FHSM_RV_FUNCTION_FAILED;",
            "}",
            "",
        ]
    lines.append("const fhsm_mech_entry_t fhsm_mechanism_table[] = {")
    for m in sorted_mechs:
        approved = 1 if m.fips == "approved" else 0
        # In strict profile, non-approved mechanisms point at reject.
        # In interop profile, they keep their real handler (audit will
        # flag every invocation).
        handler = m.handler
        if profile == "fips-strict" and not approved:
            handler = "dispatch_reject_fips"
        lines.append(
            f"    {{ 0x{m.value:08X}u, \"{m.name}\", \"{m.family}\", "
            f"\"{m.op}\", {approved}, {handler} }},"
        )
    lines += [
        "};",
        "",
        "const size_t fhsm_mechanism_count =",
        "    sizeof(fhsm_mechanism_table) / sizeof(fhsm_mechanism_table[0]);",
        "",
        "/* Binary search over the (already-sorted) table. */",
        "const fhsm_mech_entry_t *fhsm_mechanism_lookup(uint32_t ckm)",
        "{",
        "    size_t lo = 0, hi = fhsm_mechanism_count;",
        "    while (lo < hi) {",
        "        size_t mid = (lo + hi) / 2;",
        "        uint32_t v = fhsm_mechanism_table[mid].ckm_value;",
        "        if      (v == ckm) return &fhsm_mechanism_table[mid];",
        "        else if (v <  ckm) lo = mid + 1;",
        "        else               hi = mid;",
        "    }",
        "    return NULL;",
        "}",
        "",
    ]
    return "\n".join(lines)


