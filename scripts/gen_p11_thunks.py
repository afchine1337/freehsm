# gen_p11_thunks.py --- PKCS#11 v3.2 mechanism dispatch generator.
#
# Single source of truth for the mechanism table. Reading this script
# is the FIRST thing the evaluator does : every CKM_* the module
# implements MUST be listed here with its FIPS status, its target
# subsystem, its handler symbol, and its primary reference.
#
# Outputs (regenerated by `make generate`):
#
#   include/fhsm_pkcs11_mechanisms.h   CKM_* constants + extern decls
#   src/gen/fhsm_dispatch.c           dispatch table (mechanism -> handler)
#   docs/MECHANISMS.md                 human-readable reference
#
# The dispatch table is consumed by src/fhsm_pkcs11.c -- which never
# references CKM_* values directly. That decoupling lets the evaluator
# diff *this file* against the Security Policy (docs/FIPS_140_3.md §4)
# without inspecting any generated artifact.
#
# Profiles :
#   fips-strict : only FHSM_FIPS_APPROVED entries are dispatchable;
#                  legacy mechanisms are wired to dispatch_reject_fips()
#                  which returns FHSM_RV_FIPS_NOT_APPROVED at runtime.
#   interop     : every entry is dispatchable. Used only for migration
#                  from legacy systems; the audit log records every
#                  call to a non-approved mechanism.
#
# ===========================================================================

