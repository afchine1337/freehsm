# ---------------------------------------------------------------------------
# Sanity check : no duplicate values, every handler is named.
# ---------------------------------------------------------------------------
def _validate(mechs: tuple[Mech, ...]) -> None:
    seen_names = {}
    seen_values = {}
    for m in mechs:
        if m.name in seen_names:
            raise ValueError(f"duplicate name {m.name}")
        if m.value in seen_values and seen_values[m.value] != m.name:
            raise ValueError(
                f"duplicate value 0x{m.value:08X} : "
                f"{seen_values[m.value]} vs {m.name}")
        seen_names[m.name] = m
        seen_values[m.value] = m.name
        if not m.handler:
            raise ValueError(f"{m.name} : empty handler")
        if m.fips not in ("approved", "non-approved", "deprecated"):
            raise ValueError(f"{m.name} : bad fips status {m.fips!r}")


# ---------------------------------------------------------------------------
# Output 1 --- include/fhsm_pkcs11_mechanisms.h
# ---------------------------------------------------------------------------
def gen_header(mechs: tuple[Mech, ...]) -> str:
    lines = [
        "/* AUTO-GENERATED by scripts/gen_p11_thunks.py --- DO NOT EDIT. */",
        "/* Edit scripts/gen_p11_thunks.py and rerun `make generate`.   */",
        "",
        "#ifndef FHSM_PKCS11_MECHANISMS_H",
        "#define FHSM_PKCS11_MECHANISMS_H",
        "",
        "#include \"fhsm_common.h\"",
        "",
        "#ifdef __cplusplus",
        "extern \"C\" {",
        "#endif",
        "",
        "/* ---- CKM_* mechanism identifiers (PKCS#11 v3.2 §6.3) ---- */",
    ]
    # Group by family for readability
    by_family: dict[str, list[Mech]] = {}
    for m in mechs:
        by_family.setdefault(m.family, []).append(m)
    for family, group in by_family.items():
        lines.append(f"/* --- {family} --- */")
        for m in group:
            lines.append(f"#define {m.name:<36s} 0x{m.value:08X}u")
        lines.append("")
    # Handler forward declarations (single signature)
    lines += [
        "/* ---- Dispatch handler signature ---- */",
        "/*",
        " * Every CKM_* points to a handler with this signature. Concrete",
        " * handlers live in src/fhsm_dispatch_*.c (or are stubs in src/gen/",
        " * fhsm_dispatch.c for unimplemented mechanisms).",
        " *",
        " *   session_handle  --- caller's PKCS#11 session",
        " *   key_handle      --- input key handle (0 if N/A for digest)",
        " *   params, plen    --- mechanism-specific parameters (e.g.",
        " *                       CK_GCM_PARAMS, CK_RSA_PKCS_OAEP_PARAMS)",
        " *   in, inlen       --- input data slice",
        " *   out, *outlen    --- output buffer (in/out length)",
        " */",
        "typedef fhsm_rv_t (*fhsm_mech_handler_t)(",
        "    unsigned long  session_handle,",
        "    unsigned long  key_handle,",
        "    const void    *params,",
        "    size_t         plen,",
        "    fhsm_slice_t   in,",
        "    uint8_t       *out,",
        "    size_t        *outlen);",
        "",
        "/* Forward declarations of every handler symbol --- ordered by family. */",
    ]
    seen_handlers = set()
    for m in mechs:
        if m.handler in seen_handlers:
            continue
        # Skip dispatch_reject_fips here : it has its own explicit
        # declaration further down. Emitting it twice triggers
        # -Werror=redundant-decls on gcc 14+.
        if m.handler == "dispatch_reject_fips":
            continue
        seen_handlers.add(m.handler)
        lines.append(f"extern fhsm_rv_t {m.handler}(unsigned long, unsigned long, "
                      f"const void*, size_t, fhsm_slice_t, uint8_t*, size_t*);")
    lines += [
        "",
        "/* ---- Dispatch table ---- */",
        "typedef struct fhsm_mech_entry_s {",
        "    uint32_t              ckm_value;",
        "    const char           *name;",
        "    const char           *family;",
        "    const char           *operation;",
        "    int                   fips_approved;   /* 0/1 */",
        "    fhsm_mech_handler_t   handler;",
        "} fhsm_mech_entry_t;",
        "",
        "extern const fhsm_mech_entry_t fhsm_mechanism_table[];",
        "extern const size_t            fhsm_mechanism_count;",
        "",
        "/* Lookup --- O(log N), table is sorted by ckm_value. Returns NULL",
        " * if the mechanism is unknown. */",
        "const fhsm_mech_entry_t *fhsm_mechanism_lookup(uint32_t ckm);",
        "",
        "/* Standard reject handler --- returns FHSM_RV_FIPS_NOT_APPROVED.",
        " * Used as the handler for every non-approved CKM in the strict",
        " * profile build. */",
        "fhsm_rv_t dispatch_reject_fips(unsigned long, unsigned long,",
        "                                const void*, size_t, fhsm_slice_t,",
        "                                uint8_t*, size_t*);",
        "",
        "#ifdef __cplusplus",
        "}",
        "#endif",
        "",
        "#endif /* FHSM_PKCS11_MECHANISMS_H */",
        "",
    ]
    return "\n".join(lines)


