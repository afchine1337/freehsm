# ---------------------------------------------------------------------------
# Mechanism descriptor schema
# ---------------------------------------------------------------------------
@dataclass(frozen=True)
class Mech:
    """One row of the PKCS#11 v3.2 mechanism table."""
    name: str                 # "CKM_AES_GCM"
    value: int                # 0x00001087
    family: str               # "AES" / "RSA" / "ML-KEM" / ...
    op: str                   # "encrypt", "sign", "digest", "keygen",
                              # "keypair", "wrap", "derive", "encap",
                              # "decap"
    handler: str              # C symbol of the dispatch function
    fips: str                 # "approved" | "non-approved" | "deprecated"
    key_type: Optional[str] = None      # "CKK_AES" / "CKK_RSA" / ...
    min_key_bits: Optional[int] = None
    max_key_bits: Optional[int] = None
    refs: tuple[str, ...] = field(default_factory=tuple)  # ["FIPS 197"]
    notes: str = ""


