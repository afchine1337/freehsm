# CLI
# ---------------------------------------------------------------------------
def main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(
        description="Generate the PKCS#11 mechanism dispatch table.")
    p.add_argument("--profile", choices=("fips-strict", "interop"),
                    default="fips-strict",
                    help="Build profile. fips-strict rejects every "
                         "non-approved mechanism at code-gen time.")
    p.add_argument("--root", default=".",
                    help="Project root (where include/ src/ docs/ live).")
    args = p.parse_args(argv)

    _validate(MECHANISMS)
    root = Path(args.root).resolve()

    header_path   = root / "include" / "fhsm_pkcs11_mechanisms.h"
    dispatch_path = root / "src" / "gen" / "fhsm_dispatch.c"
    doc_path      = root / "docs" / "MECHANISMS.md"

    header_path.parent.mkdir(parents=True, exist_ok=True)
    dispatch_path.parent.mkdir(parents=True, exist_ok=True)
    doc_path.parent.mkdir(parents=True, exist_ok=True)

    header_path.write_text(gen_header(MECHANISMS))
    dispatch_path.write_text(gen_dispatch(MECHANISMS, args.profile))
    doc_path.write_text(gen_doc(MECHANISMS, args.profile))

    n_app = sum(1 for m in MECHANISMS if m.fips == "approved")
    n_rej = sum(1 for m in MECHANISMS if m.fips != "approved")
    print(f"[gen_p11_thunks] profile={args.profile} "
          f"approved={n_app} non-approved={n_rej} total={len(MECHANISMS)}")
    print(f"[gen_p11_thunks] wrote {header_path}")
    print(f"[gen_p11_thunks] wrote {dispatch_path}")
    print(f"[gen_p11_thunks] wrote {doc_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
