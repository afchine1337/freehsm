# Output 3 --- docs/MECHANISMS.md
# ---------------------------------------------------------------------------
def gen_doc(mechs: tuple[Mech, ...], profile: str) -> str:
    by_family: dict[str, list[Mech]] = {}
    for m in mechs:
        by_family.setdefault(m.family, []).append(m)
    lines = [
        "# FreeHSM C --- PKCS#11 v3.2 mechanism dispatch reference",
        "",
        "*Auto-generated by `scripts/gen_p11_thunks.py`. Do not edit by hand.*",
        "",
        f"**Profile generated**: `{profile}`",
        f"**Total mechanisms**: {len(mechs)}",
        f"**Approved**: {sum(1 for m in mechs if m.fips == 'approved')}",
        f"**Non-approved**: {sum(1 for m in mechs if m.fips == 'non-approved')}",
        "",
        "## Reading this table",
        "",
        "- **FIPS** column : `✅` = approved (dispatchable in fips-strict),",
        "  `❌` = non-approved (rejected with `FHSM_RV_FIPS_NOT_APPROVED`",
        "  in fips-strict, dispatchable but audited in interop).",
        "- **Op** column : the PKCS#11 operation category.",
        "- **Handler** : the C symbol invoked after argument validation.",
        "  In fips-strict, non-approved mechanisms have their handler",
        "  replaced by `dispatch_reject_fips` at code-gen time.",
        "",
    ]
    for family in sorted(by_family.keys()):
        group = by_family[family]
        lines.append(f"## {family}")
        lines.append("")
        lines.append("| Mechanism | Value | Op | FIPS | Handler | Reference |")
        lines.append("|---|---|---|---|---|---|")
        for m in group:
            mark = "✅" if m.fips == "approved" else "❌"
            refs = ", ".join(m.refs) if m.refs else "—"
            lines.append(
                f"| `{m.name}` | `0x{m.value:08X}` | {m.op} | {mark} | "
                f"`{m.handler}` | {refs} |")
            if m.notes:
                lines.append(f"| | | | | | _{m.notes}_ |")
        lines.append("")
    lines += [
        "## Cross-references",
        "",
        "- The single source of truth for this table is `scripts/gen_p11_thunks.py`.",
        "- The runtime dispatch table is generated into `src/gen/fhsm_dispatch.c`.",
        "- The public CKM_* values are generated into `include/fhsm_pkcs11_mechanisms.h`.",
        "- The Security Policy §4 (`docs/FIPS_140_3.md`) lists the same",
        "  approved algorithms grouped by family --- the evaluator can diff",
        "  the two documents to confirm conformance.",
        "",
    ]
    return "\n".join(lines)


